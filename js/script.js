window.onscroll = function showHeader() {
    var header = document.querySelector('.header');
    var link = document.querySelector('.onscroll_link');
    if(window.pageYOffset > 1){
        header.classList.add('header_fixed');
        var link = document.getElementsByClassName("onscroll_link");
        var link_length = 0;
        while(link_length < link.length){
            link[link_length].classList.add('scroll_link');
            link_length++;
        }

    } else{
        header.classList.remove('header_fixed');
        var link = document.getElementsByClassName("onscroll_link");
        var link_length = 0;
        while(link_length < link.length){
            link[link_length].classList.remove('scroll_link');
            link_length++;
        }
    }
}
